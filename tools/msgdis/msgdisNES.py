def parseHeader(buf, i):
    unk11F08 = buf[i] << 8 | buf[i + 1]
    itemId = buf[i + 2]
    nextTextId = buf[i + 3] << 8 | buf[i + 4]
    unk1206C = buf[5] << 8 | buf[i + 6]
    unk12070 = buf[7] << 8 | buf[i + 8]
    unk12074 = buf[9] << 8 | buf[i + 10]
    hdr = MessageHeader(unk11F08, itemId, nextTextId, unk1206C, unk12070, unk12074)
    return hdr

